<?php

declare(strict_types=1);

namespace App\Validator\Constraints;

use Symfony\Component\Validator\Constraint;
use Symfony\Component\Validator\ConstraintValidator;
use Symfony\Component\Validator\Exception\UnexpectedTypeException;
use Symfony\Component\Validator\Exception\UnexpectedValueException;

class RepositoryConstraintValidator extends ConstraintValidator
{
    private const REPOSITORY_REGEX_PATTERN = '/^\w*\/\w*$/';

    public function validate($value, Constraint $constraint)
    {
        if (!$constraint instanceof RepositoryConstraint) {
            throw new UnexpectedTypeException($constraint, RepositoryConstraint::class);
        }

        // custom constraints should ignore null and empty values to allow
        // other constraints (NotBlank, NotNull, etc.) take care of that
        if (null === $value || '' === $value) {
            return;
        }

        if (!is_string($value)) {
            // throw this exception if your validator cannot handle the passed type so that it can be marked as invalid
            throw new UnexpectedValueException($value, 'string');
        }
        if (false == preg_match(self::REPOSITORY_REGEX_PATTERN, $value, $matches)) {
            // the argument must be a string or an object implementing __toString()
            $this->context->buildViolation($constraint->message)
                ->setParameter('{{ string }}', $value)
                ->addViolation();
        }
    }
}
